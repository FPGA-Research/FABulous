class ConnectionHierarchyOptions(BaseModel):
    """Describe optional JUMP-based local connection hierarchy.

    Attributes
    ----------
    model_config
        Pydantic model configuration.
    enabled : bool
        Whether ordinary BEL inputs should be fed through generated JUMP stages.
    levels : list[int]
        Maximum mux fanin for each generated hierarchy level.
    generate_jump_ports : bool
        Whether tile-builder should emit generated JUMP rows into the tile CSV.
    jump_prefix : str
        Base prefix for generated JUMP wire names.
    replace_direct_input_pips : bool
        Whether hierarchy PIPs replace direct routing-source-to-BEL-input PIPs.
    """

    model_config = ConfigDict(frozen=True)

    enabled: bool = False
    levels: list[int] = Field(default_factory=lambda: [4])
    generate_jump_ports: bool = True
    jump_prefix: str = "J_LOCAL"
    replace_direct_input_pips: bool = True

    @field_validator("levels")
    @classmethod
    def _validate_levels(cls, value: list[int]) -> list[int]:
        """Validate hierarchy level fanins.

        Parameters
        ----------
        value : list[int]
            User-provided hierarchy level fanins.

        Returns
        -------
        list[int]
            Validated hierarchy levels.

        Raises
        ------
        ValueError
            If any enabled hierarchy level is smaller than two.
        """
        if any(level < 2 for level in value):
            raise ValueError("connection hierarchy levels must be at least 2")
        return value

    @field_validator("jump_prefix")
    @classmethod
    def _validate_jump_prefix(cls, value: str) -> str:
        """Validate the generated JUMP prefix.

        Parameters
        ----------
        value : str
            User-provided JUMP prefix.

        Returns
        -------
        str
            Validated JUMP prefix.

        Raises
        ------
        ValueError
            If the prefix is empty.
        """
        if not value:
            raise ValueError("jump_prefix must not be empty")
        return value

    @model_validator(mode="after")
    def _validate_enabled_levels(self) -> ConnectionHierarchyOptions:
        """Validate cross-field hierarchy settings.

        Returns
        -------
        ConnectionHierarchyOptions
            Validated hierarchy options.

        Raises
        ------
        ValueError
            If hierarchy is enabled without at least one level.
        """
        if self.enabled and not self.levels:
            raise ValueError("enabled connection hierarchy requires at least one level")
        return self


