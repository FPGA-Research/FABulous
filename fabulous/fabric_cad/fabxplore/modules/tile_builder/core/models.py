class TileBuilderResult(BaseModel):
    """Result bundle produced by one tile-builder run.

    Attributes
    ----------
    model_config
        Pydantic model configuration.
    options : TileBuilderOptions
        Normalized options used for the run.
    tile_name : str
        Generated tile name.
    tile_dir : Path
        Generated tile directory.
    tile_csv : Path
        Tile CSV path.
    matrix_list : Path | None
        Generated baseline list path, or ``None`` when FABulous auto mode is used.
    parsed_bel_modules : tuple[str, ...]
        Parsed BEL module names, one entry per instance.
    artifacts : tuple[TileBuilderArtifact, ...]
        Generated or updated artifacts.
    stats : TileBuilderStats
        Summary statistics.
    warnings : tuple[str, ...]
        Non-fatal warnings.
    report_summary : str
        Human-readable report text.
    """

    model_config = ConfigDict(frozen=True)

    options: TileBuilderOptions
    tile_name: str
    tile_dir: Path
    tile_csv: Path
    matrix_list: Path | None = None
    parsed_bel_modules: tuple[str, ...] = ()
    artifacts: tuple[TileBuilderArtifact, ...] = ()
    stats: TileBuilderStats = Field(default_factory=TileBuilderStats)
    warnings: tuple[str, ...] = ()
    report_summary: str = ""
