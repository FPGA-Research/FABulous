class RoutingPipPattern(StrEnum):
    """Routing-resource PIP pattern used for tile-local switch matrices."""

    NONE = "none"
    SUBSET = "subset"
    WILTON = "wilton"
    UNIVERSAL = "universal"


class RoutingTrackGroup(BaseModel):
    """Describe compatible routing tracks discovered from FABulous base ports.

    Attributes
    ----------
    model_config
        Pydantic model configuration.
    group_id : str
        Stable identifier used in reports and diagnostics.
    direction : Direction
        FABulous routing direction.
    x_offset : int
        X offset from the parsed FABulous port row.
    y_offset : int
        Y offset from the parsed FABulous port row.
    wire_count : int
        Number of tracks in the group.
    destination_rows : list[str]
        Switch-matrix rows driven by selectable sources.
    selectable_sources : list[str]
        Switch-matrix columns that can be selected by destination rows.
    """

    model_config = ConfigDict(frozen=True, arbitrary_types_allowed=True)

    group_id: str
    direction: Direction
    x_offset: int
    y_offset: int
    wire_count: int
    destination_rows: list[str]
    selectable_sources: list[str]


