"""Data models for FABulous tile building.

The tile builder creates a FABulous tile package from BEL RTL files and a small routing-
policy description. The models in this file keep user options, generated artifacts, and
reporting statistics structured for the builder, PnR pass wrapper, and architecture
synthesizer helper.
"""

from __future__ import annotations

from enum import StrEnum
from pathlib import Path  # noqa: TC003

from pydantic import BaseModel, ConfigDict, Field, field_validator, model_validator

from fabulous.fabric_definition.define import Direction  # noqa: TC001


